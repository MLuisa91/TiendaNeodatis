    private void inicializarTabla() {
        modeloTabla = new TablaProductos(lista);
        this.jTable1.setModel(modeloTabla);

        jTable1.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 1) {
                    int filaSeleccionada = jTable1.getSelectedRow();
                    if (filaSeleccionada != -1) {
                        producto = lista.get(filaSeleccionada);
                    }
                }
            }

        });

        final JPopupMenu popupMenu = new JPopupMenu();
        JMenuItem deleteItem = new JMenuItem("Borrar");
        deleteItem.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                GestionProducto borrar = new GestionProducto(usuario, producto, AccionesEnum.BAJA);
                borrar.setVisible(true);
                dispose();
            }
        });
        JMenuItem modifyItem = new JMenuItem("Modificar");
        modifyItem.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent ae) {
                GestionProducto modificar = new GestionProducto(usuario, producto, AccionesEnum.MODIFICACION);
                modificar.setVisible(true);
                dispose();
            }
        });
        popupMenu.add(deleteItem);
        popupMenu.add(modifyItem);
        jTable1.setComponentPopupMenu(popupMenu);

    }

    private void aplicarFiltros() {
        String precio = jTextFieldPrecio.getText();
        String stock = jTextFieldStock.getText();
        String nombre = jTextFieldNombre.getText();
        Float precioFloat = null;
        Integer stockEntero = null;
        
        if (!precio.isEmpty())
             precioFloat = Float.parseFloat(precio);
        
        if(!stock.isEmpty())
            stockEntero = Integer.parseInt(stock);

        Producto p = new Producto(null, nombre, precioFloat, stockEntero);

        List<Producto> filtrados = crudProductos.search(p);

        modeloTabla = new TablaProductos(filtrados);
        this.jTable1.setModel(modeloTabla);
    }

