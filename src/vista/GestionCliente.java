    private void jButtonVolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVolverActionPerformed
        if (AccionesEnum.ALTA.equals(accion)) {
            BienvenidoUsuario hola = new BienvenidoUsuario(usuario);
            dispose();
            hola.setVisible(true);
        } else {
            ListarCliente listarCliente = new ListarCliente(usuario, this, true);
            dispose();
            listarCliente.setVisible(true);

        }
    }//GEN-LAST:event_jButtonVolverActionPerformed

    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // validamos que no haya una cuenta con el mismo numero ya existente

        // si no hay ninguna cuenta con el mismo número la damos de alta
        if (AccionesEnum.ALTA.equals(accion)) {
            Cliente cliente = recogerDatos();

            String dni = jTextFieldDni.getText();
            List<Usuario> misClientes = crudClientes.search(new Cliente(null, dni, null, null, null));
            if (misClientes.isEmpty()) {
                if (cliente != null && crudClientes.add(cliente)) {
                    JOptionPane.showMessageDialog(this, "Operación realizada correctamente.");
                    BienvenidoUsuario hola = new BienvenidoUsuario(usuario);
                    dispose();
                    hola.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Se ha producido un error.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Ya hay un cliente con el mismo dni.");
            }
        }
        if (AccionesEnum.BAJA.equals(accion)) {
            if (cliente != null && crudClientes.delete(cliente)) {
                JOptionPane.showMessageDialog(this, "Operación realizada correctamente.");
                ListarCliente listarCliente = new ListarCliente(usuario, this, true);
                dispose();
                listarCliente.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Se ha producido un error.");
            }

        }
        if (AccionesEnum.MODIFICACION.equals(accion)) {
            Cliente cliente = recogerDatos();
            if (cliente != null && crudClientes.update(cliente)) {
                JOptionPane.showMessageDialog(this, "Operación realizada correctamente.");
                ListarCliente listarCliente = new ListarCliente(usuario, this, true);
                dispose();
                listarCliente.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Se ha producido un error.");
            }
        }

    }//GEN-LAST:event_jButtonAceptarActionPerformed


