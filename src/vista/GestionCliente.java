    private Cliente recogerDatos() {
        Cliente cliente = null;
        boolean correcto = true;
        String errores = "";
        
        String nombre = jTextFieldNombre.getText();
        if(nombre.isEmpty()){
             errores = errores + "- El campo nombre es obligatorio.\n";
             correcto = false;
        }
               
        String apellidos = jTextFieldApellidos.getText();
        if(apellidos.isEmpty()){
             errores = errores + "- El campo apellidos es obligatorio.\n";
             correcto = false;
        }
        String direccion = jTextFieldDireccion.getText();
        if(direccion.isEmpty()){
             errores = errores + "- El campo dirección es obligatorio.\n";
             correcto = false;
        }
        String dni = jTextFieldDni.getText();
        if(dni.isEmpty()){
             errores = errores + "- El campo dni es obligatorio.\n";
             correcto = false;
        }
        
        if(correcto == true){
            cliente = new Cliente(false, dni, nombre, apellidos, direccion);
        }else{
            JOptionPane.showMessageDialog(this, errores);
        }

       

        return cliente;
    }

    private void mostrarDatosHabilitados(Cliente cliente) {

        jTextFieldNombre.setText(cliente.getNombre());
        jTextFieldApellidos.setText(cliente.getApellidos());
        jTextFieldDireccion.setText(cliente.getDireccion());
        jTextFieldDni.setText(cliente.getDni());
        // DNI lo tenemos establecido como primary key así que se tiene que mostrar deshabilitado
        jTextFieldDni.setEnabled(false);

    }

    private void mostrarDatosDeshabilitados(Cliente cliente) {

        jTextFieldNombre.setText(cliente.getNombre());
        jTextFieldNombre.setEnabled(false);
        jTextFieldApellidos.setText(cliente.getApellidos());
        jTextFieldApellidos.setEnabled(false);
        jTextFieldDireccion.setText(cliente.getDireccion());
        jTextFieldDireccion.setEnabled(false);
        jTextFieldDni.setText(cliente.getDni());
        jTextFieldDni.setEnabled(false);

    }

