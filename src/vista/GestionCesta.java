    private void inicializarTabla() {
        modeloTabla = new TablaProductosCesta(lista);
        this.jTable1.setModel(modeloTabla);
        jTable1.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 1) {
                    int filaSeleccionada = jTable1.getSelectedRow();
                    if (filaSeleccionada != -1) {
                        producto = lista.get(filaSeleccionada);
                    }
                }
            }

        });
    }

    private void cargarComboProducto() {
        crudProductos.getProductos().stream().forEach(producto -> this.jComboBoxProductos.addItem(new ComboItemProductos(producto)));
    }

    private Cesta recogerDatos() {
        Cesta cesta = null;
        boolean correcto = true;
        String errores = "";

        String nombre = jTextFieldNombre.getText();
        if (nombre.isEmpty()) {
            errores = errores + "- El campo nombre es obligatorio.\n";
            correcto = false;
        }

        Integer id = Integer.parseInt(jTextFieldID.getText());

        if (id == null) {
            errores = errores + "- El campo nombre es obligatorio.\n";
            correcto = false;
        }

        Float total = Float.parseFloat(jTextFieldTotal.getText());

        if (correcto == true) {
            cesta = new Cesta(id, nombre, cliente, lista, total);
        } else {
            JOptionPane.showMessageDialog(this, errores);
        }

        return cesta;

    }

