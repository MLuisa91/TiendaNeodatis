    private void inicializarTabla() {

        modeloTabla = new TablaClientes(lista);
        this.jTable.setModel(modeloTabla);

        jTable.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 1) {
                    int filaSeleccionada = jTable.getSelectedRow();
                    if (filaSeleccionada != -1) {
                        cliente = lista.get(filaSeleccionada);
                    }
                }
            }

        });

        final JPopupMenu popupMenu = new JPopupMenu();
        JMenuItem deleteItem = new JMenuItem("Borrar");
        deleteItem.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                GestionCliente borrar = new GestionCliente(cliente, AccionesEnum.BAJA);
                borrar.setVisible(true);
                dispose();
            }
        });
        JMenuItem modifyItem = new JMenuItem("Modificar");
        modifyItem.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent ae) {
                GestionCliente modificar = new GestionCliente(cliente, AccionesEnum.MODIFICACION);
                modificar.setVisible(true);
                dispose();
            }
        });
        popupMenu.add(deleteItem);
        popupMenu.add(modifyItem);
        jTable.setComponentPopupMenu(popupMenu);

    }

    private void aplicarFiltros() {
        String nombre = jTextFieldNombre.getText();
        String primerApellido = jTextFieldApellidos.getText();
        String direccion = jTextFieldDireccion.getText();
        String dni = jTextFieldDni.getText();


        Cliente c = new Cliente(dni, nombre, direccion, direccion);

        List<Cliente> filtrados = crudClientes.search(c);

        modeloTabla = new TablaClientes(filtrados);
        this.jTable.setModel(modeloTabla);
    }
